package com.example.android.alzmate.DairyDetail;

public class MessageHolder {
    private String title;
    private String body;

    public MessageHolder(String title, String body) {
        this.title = title;
        this.body = body;
    }

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }
}
